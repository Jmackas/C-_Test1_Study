
/****************************************
Try catch block 
****************************************/
// syntax
	try {
	 // all code between here
	} catch {
	 MessageBox.Show("Code Dump");
	}
	
// example
	try {
		decimal paintArea, paintPrice;
	} catch {
	 MessageBox.Show(/* code */);
	}
	
	
	
